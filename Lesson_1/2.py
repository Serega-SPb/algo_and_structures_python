# 2. Выполнить логические побитовые операции "И", "ИЛИ" и др.
# над числами 5 и 6. Выполнить
# над числом 5 побитовый сдвиг вправо и влево на два знака.

m = 5
n = 6

b_and = m & n
b_or = m | n
b_xor = m ^ n

m_r_shift = m >> 2
m_l_shift = m << 2

print(f'{m} AND {n} = {b_and}\n{m} OR {n} = {b_or}\n{m} XOR {n} = {b_xor} ')
print(f'{m} сдвиг вправо на два знака - {m_r_shift}\n{m} сдвиг влево на два знака - {m_l_shift}')
